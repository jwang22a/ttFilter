package entity;

public class ProcessEntity {
	public String processName;//流程名称
	public String processNo;//流程编号
	public String managerNmae;//流程管理员姓名
	public String managerOA;//流程管理员OA
	public String getProcessName() {
		return processName;
	}
	public void setProcessName(String processName) {
		this.processName = processName;
	}
	public String getProcessNo() {
		return processNo;
	}
	public void setProcessNo(String processNo) {
		this.processNo = processNo;
	}
	public String getManagerNmae() {
		return managerNmae;
	}
	public void setManagerNmae(String managerNmae) {
		this.managerNmae = managerNmae;
	}
	public String getManagerOA() {
		return managerOA;
	}
	public void setManagerOA(String managerOA) {
		this.managerOA = managerOA;
	}
}
